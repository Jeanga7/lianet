"use client";

import { useEffect, useState } from "react";
import { motion, AnimatePresence } from "framer-motion";
import { useI18n } from "@/lib/useI18n";
import { Magnetic } from "@/components/ui";

export default function WhatsAppButton() {
    const { t } = useI18n();
    const [isVisible, setIsVisible] = useState(false);
    const phoneNumber = "+221778756631"; // Updated with user's real number

    useEffect(() => {
        // Apparition aprÃ¨s 5s ou 300px de scroll
        const timer = setTimeout(() => setIsVisible(true), 5000);

        const handleScroll = () => {
            if (window.scrollY > 300) {
                setIsVisible(true);
            }
        };

        window.addEventListener("scroll", handleScroll);
        return () => {
            clearTimeout(timer);
            window.removeEventListener("scroll", handleScroll);
        };
    }, []);

    const handleClick = () => {
        window.open(`https://wa.me/${phoneNumber.replace("+", "")}`, "_blank");
    };

    return (
        <AnimatePresence>
            {isVisible && (
                <motion.div
                    initial={{ opacity: 0, scale: 0.5, y: 20 }}
                    animate={{ opacity: 1, scale: 1, y: 0 }}
                    exit={{ opacity: 0, scale: 0.5, y: 20 }}
                    className="fixed bottom-6 right-6 z-[100] lg:bottom-10 lg:right-10"
                >
                    <Magnetic strength={20}>
                        <button
                            onClick={handleClick}
                            className="group relative flex h-14 w-14 items-center justify-center rounded-full bg-[#1B365D]/85 text-white backdrop-blur-xl border border-white/20 shadow-[0_20px_50px_rgba(27,54,93,0.3)] transition-all duration-300 hover:scale-105 active:scale-95 lg:h-16 lg:w-16"
                            aria-label={t("common.whatsapp.tooltip")}
                        >
                            {/* Overlay Glass subtle */}
                            <div className="absolute inset-0 rounded-full bg-gradient-to-tr from-[#40B4A6]/20 to-transparent opacity-0 group-hover:opacity-100 transition-opacity duration-500" />

                            {/* WhatsApp Icon - Pure SVG for Elite look */}
                            <svg
                                viewBox="0 0 24 24"
                                className="relative z-10 h-7 w-7 fill-white lg:h-8 lg:w-8 transition-transform group-hover:scale-110"
                                xmlns="http://www.w3.org/2000/svg"
                            >
                                <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L0 24l6.335-1.662c1.72.937 3.659 1.433 5.626 1.433h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                            </svg>

                            {/* Tooltip hint on hover (Desktop) */}
                            <div className="absolute right-full mr-5 hidden pointer-events-none items-center lg:flex opacity-0 group-hover:opacity-100 transition-all duration-300 translate-x-2 group-hover:translate-x-0">
                                <div className="bg-[#1B365D]/95 text-white text-[10px] uppercase tracking-[0.2em] font-bold py-2.5 px-5 rounded-full whitespace-nowrap shadow-2xl border border-white/10 backdrop-blur-xl">
                                    {t("common.whatsapp.tooltip")}
                                </div>
                            </div>

                            {/* Pulse Effect - Turquoise lianet theme */}
                            <div className="absolute inset-0 rounded-full animate-ping bg-[#40B4A6]/30 pointer-events-none opacity-40" />
                        </button>
                    </Magnetic>
                </motion.div>
            )}
        </AnimatePresence>
    );
}
